# A variable is a container for a value. It behaves as the value that it contains

first_name = "Jurgen" # Strings can use single or double quotes
last_name = "Alfaro"
full_name = first_name + " " + last_name

print("Hello " + full_name)
print(type(full_name)) # <class 'str'>

age = 28 # integer
print(age)
print(type(age)) # <class 'int'>

height = 250.5 # float
print(height)
print(type(height)) # <class 'float'>

human = True # boolean
print(human)
print(type(human)) # <class 'bool'>

